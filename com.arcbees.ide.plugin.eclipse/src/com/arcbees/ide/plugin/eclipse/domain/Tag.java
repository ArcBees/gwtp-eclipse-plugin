package com.arcbees.ide.plugin.eclipse.domain;

public class Tag {
    private String key;
    private String name;
    
    public Tag() {
    }

    public String getKey() {
        return key;
    }

    public void setKey(String key) {
        this.key = key;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
